// gen-globe-coast.mjs :: regenerate the LAND coastline rings for globe.ts from
// Natural Earth 110m land (scripts/ne_110m_land.json). The globe no longer paints
// a dot-matrix "leather" Earth; it strokes clean glowing coastline LINE-ART, so
// the land data must now be a set of SEPARATE coastline rings, never a dilated or
// concatenated soup of loops.
//
// The earlier gen-globe-land.mjs DILATED each ring 0.5 deg outward to pull coastal
// cities onto land for the point-in-polygon dot test. That dilation bridged narrow
// oceans and is exactly what made the Americas visually collide into Europe/Africa
// and Asia. This generator does NOT dilate.
//
// Pipeline:
//   1. read ne_110m_land.json (Polygon features; outer + hole rings),
//   2. emit EACH ring of EACH Polygon/MultiPolygon as ITS OWN flat [lon,lat,...]
//      loop. Rings are NEVER concatenated: joining two rings into one loop creates
//      a spurious long edge that visually bridges continents (the exact bug),
//   3. DROP Antarctic rings (maxlat < -60); the analytic isAntarctica band in
//      globe.ts paints the southern cap. Antarctica is also the only ring that
//      crosses the antimeridian, so dropping it keeps every kept loop within
//      +/-180 (no huge dateline-wrap edge),
//   4. DROP rings whose bounding-box diagonal is below DIAG_MIN degrees (the
//      smallest islands) to keep the ring count lean; all major landmasses and
//      large islands (Greenland, Madagascar, Britain, Japan, New Zealand, Borneo,
//      Iceland, Sri Lanka, Taiwan, ...) clear this floor comfortably,
//   5. ROUND to 1 decimal,
//   6. WRITE BOTH:
//        (a) src/renderer/panels/globe/land-data.ts  -> export const LAND
//        (b) scripts/land-data.json                  -> the identical array
//      so the renderer and the self-test read byte-identical data.
//
// Usage: node scripts/gen-globe-coast.mjs

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = dirname(HERE);

const DIAG_MIN = 1.5;          // deg; bbox-diagonal floor (drops micro-islands)
const ANTARCTIC_MAXLAT = -60;  // analytic isAntarctica band paints the cap

const round1 = (n) => Math.round(n * 10) / 10;

function ringBBox(ring) {
  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;
  for (const [x, y] of ring) {
    if (x < minX) minX = x;
    if (x > maxX) maxX = x;
    if (y < minY) minY = y;
    if (y > maxY) maxY = y;
  }
  return { minX, minY, maxX, maxY };
}

export function buildLand() {
  const raw = JSON.parse(readFileSync(join(HERE, 'ne_110m_land.json'), 'utf8'));
  const loops = [];
  for (const f of raw.features) {
    const g = f.geometry;
    const polys = g.type === 'Polygon' ? [g.coordinates] : g.coordinates;
    for (const poly of polys) {
      // EVERY ring (outer ring + any holes) stays separate. A hole stroked as its
      // own closed loop reads as an inner coastline (Caspian shore etc.); it is
      // never merged into the outer ring, so no bridging edge is created.
      for (const ring of poly) {
        const b = ringBBox(ring);
        if (b.maxY < ANTARCTIC_MAXLAT) continue; // analytic isAntarctica cap
        const diag = Math.hypot(b.maxX - b.minX, b.maxY - b.minY);
        if (diag < DIAG_MIN) continue; // drop the smallest islands
        const flat = [];
        for (const [x, y] of ring) {
          const lon = x < -180 ? -180 : x > 180 ? 180 : x;
          const lat = y < -90 ? -90 : y > 90 ? 90 : y;
          flat.push(round1(lon), round1(lat));
        }
        loops.push(flat);
      }
    }
  }
  // Largest first so isLand() finds the common case quickly (mirrors the old order).
  loops.sort((a, b) => b.length - a.length);
  return loops;
}

/** Pretty-but-compact LAND block: each ring on its own indented set of lines. */
function emitTs(loops) {
  const lines = [];
  lines.push('// land-data.ts :: GENERATED by scripts/gen-globe-coast.mjs. DO NOT EDIT BY HAND.');
  lines.push('// Clean Natural Earth 110m coastline rings (NOT dilated, NOT concatenated):');
  lines.push('// each Polygon/MultiPolygon ring is its own flat [lon, lat, lon, lat, ...] loop,');
  lines.push('// degrees, rounded to 1 decimal. Antarctic + micro-island rings are dropped.');
  lines.push('// Regenerate: node scripts/gen-globe-coast.mjs');
  lines.push('export const LAND: number[][] = [');
  for (const loop of loops) {
    const parts = [];
    for (let i = 0; i < loop.length; i += 16) {
      parts.push('  ' + loop.slice(i, i + 16).join(', '));
    }
    lines.push('  [\n' + parts.join(',\n') + '\n  ],');
  }
  lines.push('];');
  return lines.join('\n') + '\n';
}

const loops = buildLand();

let verts = 0;
for (const l of loops) verts += l.length / 2;

const tsPath = join(ROOT, 'src', 'renderer', 'panels', 'globe', 'land-data.ts');
const jsonPath = join(HERE, 'land-data.json');
writeFileSync(tsPath, emitTs(loops), 'utf8');
writeFileSync(jsonPath, JSON.stringify(loops), 'utf8');

process.stderr.write(`rings kept: ${loops.length}   total vertices: ${verts}\n`);
process.stderr.write(`wrote ${tsPath}\n`);
process.stderr.write(`wrote ${jsonPath}\n`);
